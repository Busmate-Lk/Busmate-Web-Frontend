// Helper function to get schedules by route ID
export const getSchedulesByRouteId = (
  routeId: string
): ScheduleManagementResponse[] => {
  return dummySchedules.filter((schedule) => schedule.routeId === routeId);
};

// Helper function to get schedule by ID
export const getScheduleById = (
  scheduleId: string
): ScheduleManagementResponse | undefined => {
  return dummySchedules.find((schedule) => schedule.id === scheduleId);
};

// Helper function to get schedules by direction
export const getSchedulesByDirection = (
  routeId: string,
  direction: 'forward' | 'backward'
): ScheduleManagementResponse[] => {
  return dummySchedules.filter(
    (schedule) =>
      schedule.routeId === routeId && schedule.scheduleType === direction
  );
};

